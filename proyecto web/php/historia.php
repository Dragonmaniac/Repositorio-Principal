<div>
  <p class="algo">La historia de la comunidad de El Centro está ligada a la historia de Rubio. Su inicio se debe al impulso del cultivo, producción y comercialización del café en la zona. El Centro de Rubio, según la historiadora y profesora Libia Suarez de Peñaloza, en su libro "Rubio y su Historia, proceso de poblamiento de un pueblo de frontera", señala que la comunidad del centro se inicia a partir de 1872, con la llegada de la visita personal por parte del señor Obispo de Mérida, Dr. Juan Hilario Bosset. Por otro lado e igualmente, el cronista de la cuidad, Jesús Acevedo Sánchez, en su libro "Tierras del Municipio Junín", expresa que:
En los potreros llamados "Los Mangos", de la antigua hacienda La Yegüera, el señor Juan Entrena, dueño de la propiedad, dona los terrenos para la construcción del templo, plaza pública, espacio para las calles y se compromete a la venta de siete fanegadas para el desarrollo de la nueva población de Rubio.</p> </div>
<br>
<div id="contenedor-center">
  <div id="texto-L">
    <p class="algo">Apartir de ese momento, Rubio, ya es otra cosa. El entorno geográfico comienza a desarrollarse y a diseñarse topográficamente; se construía la la cuidad. La oligarquía que se había formado con la rentabilidad de la producción del café, trae a un agrimensor extranjero para que diseñara la ciudad que querían. Se diseñaron grandes calles que hoy son el centro de Rubio. Se construyeron casas amplias y holgadas. Y llama la atención que las casas no eran solo residencias, sino también casas comerciales. </p>
  </div>
  <center><img src="imagenes/rubio_5.jpg" width="197" height="250" /></center>
