    <p class="algo"> Desde entonces la planificación de la nueva propuesta de la ciudad, el centro es la zona del comercio y donde reposan las principales oficinas de las instituciones públicas de la ciudad. También cuenta con la majestuosa iglesia Santa Barbará de Rubio, así mismo se hallan una serie de intituciones educativas de gran prestigio que han formado centenares de profesionales nativos de la localidad.</p>
